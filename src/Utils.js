
export const suma = (a, b) => {
  return a + b;
};

export const resta = (a, b) => {
  return a - b;
};
export const multi = (a, b) => {
  return a * b;
};
export const div = (a, b) => {
  return a / b;
};
export const pot = (a, b) => {
  return Math.pow(a, b)
};
export const raiz = (b) => {
  return Math.sqrt(b)
};